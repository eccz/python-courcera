# Затем по этому ряду бросили K шаров, при этом i-й шар сбил все кегли с номерами от lᵢ до rᵢ включительно.
# Определите, какие кегли остались стоять на месте.
# Программа получает на вход количество кеглей N и количество бросков K.
# Далее идет K пар чисел lᵢ, rᵢ, при этом 1 ≤ lᵢ ≤ rᵢ ≤ N ≤ 100.
# Программа должна вывести последовательность из N символов, где j-й символесть “I”,
# если j-я кегля осталась стоять, или “.”, если j-я кегля была сбита.

