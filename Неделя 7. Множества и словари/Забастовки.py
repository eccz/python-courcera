frstinp = list(map(int, input().split()))
days = frstinp[0]
parties = frstinp[1]
bast = set()
for i in range(parties):
    fday, delta = map(int, input().split())
    # dates = (i for i in range(fday, days + 1, delta)
    #          if i % 7 != 0 and i % 7 != 6)
    for j in range(fday, days + 1, delta):
        if j not in bast and j % 7 != 0 and j % 7 != 6:
            bast.add(j)
print(len(bast))

